# 산술연산자
# + - * / // % **
print(2 + 3) # 5
print(2 - 3) # -1
print(2 * 3) # 6
print(2 / 3) # 0.666666
print(2 // 3) # 0
print(2 % 3) # 2
print(2 ** 3) # 8
# 몫과 나머지를 튜플 형태로 반환해주는 내장 함수... 
print(divmod(2,3))
 
# 비교연산자
# == != > < >= <=
print(5 == 5) # True
print(5 != 5) # False
print(5 == '5') # False
print(5 > 4)    # True
print(5 <= 4)   # False

